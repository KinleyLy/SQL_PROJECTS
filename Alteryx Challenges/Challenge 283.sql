SELECT 
    NOC AS COUNTRY,
    COUNT(RECORDID) AS ATHLETE_COUNT
FROM AYX283_ATHLETES
GROUP BY COUNTRY;
                                                //CHALLENGE 283 Q2
SELECT 
    NOC AS COUNTRY,
    COUNT(RECORDID) AS ATHLETE_COUNT
FROM AYX283_ATHLETES
GROUP BY COUNTRY
ORDER BY ATHLETE_COUNT DESC
LIMIT 5;
                                                //CHALLENGE 283 Q3
SELECT 
    NOC AS COUNTRY,
    COUNT(RECORDID) AS ATHLETE_COUNT
FROM AYX283_ATHLETES
GROUP BY COUNTRY
ORDER BY ATHLETE_COUNT ASC, COUNTRY ASC 
LIMIT 5;
                                                //CHALLENGE 283 Q4
