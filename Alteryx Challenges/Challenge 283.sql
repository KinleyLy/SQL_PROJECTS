    NOC AS COUNTRY,
    DISCIPLINE,
    COUNT(RECORDID) AS ATHLETE_COUNT
FROM AYX283_ATHLETES
GROUP BY COUNTRY, DISCIPLINE
ORDER BY COUNTRY ASC, DISCIPLINE ASC;
